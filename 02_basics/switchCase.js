const key = "hello";

switch (key) {
    case 0:
        console.log("January")
        break;
    case 1:
        console.log("Feb")
        break;
    case 2:
        console.log("March")
        break;
    case 3:
        console.log("April")
        break;
    case "hello":
        console.log("May")
        break;
    case 5:
        console.log("June")
        break;
    case 6:
        console.log("July")
        break;

    default:
        console.log("default case trigger")
        break;
}